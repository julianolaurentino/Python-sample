# Criando uma calculadora

a = int(input("Digite o Primeiro número aqui: "))
b = int(input("Digite o Segundo número aqui: "))

soma = a + b
subtracao = a - b
multiplicacao = a * b
divisao = a / b
resto = a % b
print('\n''Resultado de sua calculadora: ')
print(f'Soma:{soma}''\n'f'Subtração:{subtracao}''\n'f'Multiplicação:{multiplicacao}''\n'f'Divisão:{divisao}''\n'f'Resto:{resto}')
 
